from __future__ import annotations

from dataclasses import dataclass
from datetime import timedelta

from .models import AuditEvent, Card, Column, utc_now


@dataclass(frozen=True, slots=True)
class DemoEventSpec:
    hours_ago: float
    actor: str
    action: str
    message: str
    details: dict
    source: str = "ui"


@dataclass(frozen=True, slots=True)
class DemoCardSpec:
    id: str
    title: str
    description: str
    column: str
    tags: tuple[str, ...]
    total_seconds: int
    remaining_seconds: int
    archived: bool
    events: tuple[DemoEventSpec, ...]


def _split_demo_heading(value: str) -> tuple[str, str]:
    if " / " not in value:
        return "", value
    vehicle, title = value.split(" / ", 1)
    return vehicle.strip(), title.strip()


def _event(
    hours_ago: float,
    actor: str,
    action: str,
    message: str,
    details: dict | None = None,
    *,
    source: str = "ui",
) -> DemoEventSpec:
    return DemoEventSpec(
        hours_ago=hours_ago,
        actor=actor,
        action=action,
        message=message,
        details=dict(details or {}),
        source=source,
    )


def _card(
    *,
    card_id: str,
    title: str,
    description: str,
    column: str,
    tags: tuple[str, ...],
    total_hours: int,
    remaining_hours: float,
    events: tuple[DemoEventSpec, ...],
    archived: bool = False,
) -> DemoCardSpec:
    return DemoCardSpec(
        id=card_id,
        title=title,
        description=description,
        column=column,
        tags=tags,
        total_seconds=total_hours * 3600,
        remaining_seconds=int(remaining_hours * 3600),
        archived=archived,
        events=events,
    )


def build_demo_board(existing_settings: dict | None = None) -> dict:
    settings = dict(existing_settings or {})
    settings["demo_seeded"] = True

    columns = [
        Column(id="priemka", label="ПРИЁМКА", position=0),
        Column(id="diagnostics", label="ДИАГНОСТИКА", position=1),
        Column(id="electrics", label="ЭЛЕКТРИКИ", position=2),
        Column(id="mechanics", label="СЛЕСАРЯ", position=3),
        Column(id="body", label="КУЗОВ", position=4),
        Column(id="machining", label="ТОКАРКА", position=5),
        Column(id="parts", label="ЗАПЧАСТИ", position=6),
        Column(id="approval", label="СОГЛАСОВАНИЕ", position=7),
        Column(id="delivery", label="ВЫДАЧА", position=8),
    ]

    now = utc_now()
    cards: list[Card] = []
    events: list[AuditEvent] = []

    for spec in _demo_specs():
        vehicle, title = _split_demo_heading(spec.title)
        event_timestamps = [now - timedelta(hours=item.hours_ago) for item in spec.events]
        created_at = min(event_timestamps) if event_timestamps else now
        updated_at = max(event_timestamps) if event_timestamps else now
        deadline_timestamp = now + timedelta(seconds=max(0, spec.remaining_seconds))
        if spec.remaining_seconds < 0:
            deadline_timestamp = now - timedelta(seconds=abs(spec.remaining_seconds))

        cards.append(
            Card(
                id=spec.id,
                title=title,
                description=spec.description,
                column=spec.column,
                archived=spec.archived,
                created_at=created_at.isoformat(),
                updated_at=updated_at.isoformat(),
                deadline_timestamp=deadline_timestamp.isoformat(),
                deadline_total_seconds=spec.total_seconds,
                vehicle=vehicle,
                tags=list(spec.tags),
            )
        )

        for index, item in enumerate(spec.events, start=1):
            events.append(
                AuditEvent(
                    id=f"{spec.id}_event_{index}",
                    timestamp=(now - timedelta(hours=item.hours_ago)).isoformat(),
                    actor_name=item.actor,
                    source=item.source,  # type: ignore[arg-type]
                    action=item.action,
                    message=item.message,
                    details=dict(item.details),
                    card_id=spec.id,
                )
            )

    events.sort(key=lambda item: item.timestamp)
    return {
        "columns": columns,
        "cards": cards,
        "events": events,
        "settings": settings,
    }


